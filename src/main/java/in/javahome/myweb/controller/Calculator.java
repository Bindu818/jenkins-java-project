public class Calculator {
	
	/**
	 * @param i First number
	 * @param j Second number
	 * @return int
	 */
	public int add(int i, int j){
		return i+j;
	}
	public int multiply(int i, int j){
		return i*j;
	}
	
	public int sub(int i, int j){
		return i-j;
	}
}
